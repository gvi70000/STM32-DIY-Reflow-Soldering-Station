static float calculateNTCTemperature(uint8_t idx, uint16_t* adcValues) {
  uint32_t sum = 0;
  for (uint8_t i = 0; i < ADC_SIZE; i++) {
    sum += adcValues[i];
  }
  float avgAdcValue = (float)sum / ADC_SIZE;
  float r_ntc = R2[idx] * (ADC_RES / avgAdcValue - 1.0);
  return (1.0 / (INV_T0 + 1.0 / Beta * log(r_ntc / R1[idx])) - K);
}

//Resistors as per link below, R1(Rntc) is the NTC and R2 is 100kOhm
//https://ohmslawcalculator.com/voltage-divider-calculator
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
  if (hadc->Instance == ADC1) {
    t1 = calculateNTCTemperature(0, ntc1_Val);
  }
  if (hadc->Instance == ADC2) {
    t2 = calculateNTCTemperature(1, ntc2_Val);
  }
	float medT = (t1 + t2) / 2.0;
	float medT2 = medT*medT;
  crtTemp = -0.0006*medT2 + 1.2193*medT +2.3892;
}
